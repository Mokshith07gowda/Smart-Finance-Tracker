// For remaining languages, fall back to English for missing keys
const fallback = (lang, key) => (translations[lang] && translations[lang][key]) || translations.en[key] || key;

export const LanguageProvider = ({ children }) => {
  const [language, setLanguage] = useState(() => {
    return localStorage.getItem('appLanguage') || 'en';
  });

  const changeLanguage = useCallback((langCode) => {
    setLanguage(langCode);
    localStorage.setItem('appLanguage', langCode);
  }, []);

  const t = useCallback((key) => {
    return fallback(language, key);
  }, [language]);

  return (
    <LanguageContext.Provider value={{ language, changeLanguage, t, languages: LANGUAGES }}>
      {children}
    </LanguageContext.Provider>
  );
};
